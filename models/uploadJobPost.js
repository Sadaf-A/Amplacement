const mongoose = require("mongoose");

let uploadJobPostSchema = new mongoose.Schema({
  companyId: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  salary: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("NewJobPostUpload", uploadJobPostSchema);